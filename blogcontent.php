 		    <h3 class="blogoverskrifter">Guide til en hyggelig have</h3>
 		    <p><i>Skrevet 04-06-2017</i></p>
  		    
            <p class="tekstmargin">
  			Trænger din terrasse til en kærlig hånd, eller er du lige flyttet i hus med en lækker have? 

			Det kan være svært at finde ud af, hvor man skal starte i "projekt hyggelig terrasse" - skal det være det mørke eller lyse havebord, stolene i flet eller træ, og hvilken parasol passer mon ind? </p>

			<p class="tekstmargin">
			Vi har samlet vores bedste tips til at indrette en hyggelig terrasse, der kan bruges hele året rundt! 

			Hvis du, som os, godt kan lide at kunne bruge dine have møbler hele året rundt, foreslår vi, at du køber et sæt i metal – de kan tåle både regn og sne, og på den måde behøver du ikke at rykke dem inden for, når vi sætter uret tilbage.  

			For at gøre metalmøblerne hyggeligere kan du med fordel bruge en masse puder og tæpper i indretningen – og så lige tilføje en hyndeboks, så du ikke skal finde plads til at opbevare puderne inden for i regnvejr! Og så er det ellers bare at gå amok i boho-hygge – lanterner, lyskæder og planter tilføjer hygge og personlighed. 
  		    </p>
  		    
            <p class="tekstmargin">
  		    	Den sidste ting du mangler for at gøre din terrasse hyggelig er selvfølgelig en parasol og parasolfod! Selvom det er begrænset, hvor mange sommerdage vi har i Danmark, kan det alligevel være rart at kunne finde skygge. Derudover kan du også bruge en parasol som
  		    	læ for regnvejr! 
				For at kunne lade din parasolfod stå ude hele året rundt, anbefaler vi vores PLINTHIT parasolfod.  
			</p>
			
			<h4 class="tekstmargin-header">Den perfekte parasolfod</h4>
			<p>
				PLINTHIT Parasolfoded passer til stort set alle parasolstokke fra 28-58 mm, og du kan derfor frit vælge lige den parasol du synes passer ind på din terrasse. Den er ekstrem slidstærk, da den er støbt i en blanding af nylon og glas – slut med parasolfødder der revner i frost! 
			</p>
			<p class="tekstmargin">
				Til PLINTHIT Parasolfod hører der to stofposer og plastikposer til. Når du har modtaget din parasolfod, putter du blot plastikposen ned i stofposen, og fylder denne med sand! Resultatet er to sandsække, som garanterer, at parasolfodden kan modstå vind op til 40 km/t - og det er altså ikke så lidt!  
			</p>
			<p class="tekstmargin">
				Stofposerne er lavet af polypropylene, som et slidstærkt stof, der er modstandsdygtig overfor mug, skimmel, alger og UV-stråler. Stofposerne er ikke direkte vandafvisende, men absorberer ikke vandet og tørrer derfor hurtigt efter et regnskyl – så heller ikke dem skal du flytte ind og ud. Et ekstra plus er, at de er nemme at håndtere, og forbliver flotte efter mange sæsoner.  
			</p>
			<p class="tekstmargin">
				Du kan vælge imellem en parasolfod med op til 30 kg sandsække eller 50 kg. Hvis du bor i et område med meget vind, f.eks. ud til kysterne, anbefaler vi at du vælger den på 50 kg.  

				PLINTHIT Parasolfod vil altså gøre det nemmere for dig at flytte parasollen rundt i forhold til solen, og alle medlemmer i familien kan lige pludselig hjælpe til her. Parasolfoden vil ikke mindst være et godt sted at samle familien hele året rundt, både til morgen, middag og aften. 
  		    </p>  		    
  		        
